def get_tier_range(c, tier):
    rows = {r[0]: int(r[1]) for r in c.execute(
        "SELECT key, value FROM pricing_settings WHERE key LIKE ?",
        (f"id_tier_{tier}_%",)
    )}
    # Default fallback
    defaults = {
        "A": (800_000, 1_000_000),
        "B": (600_000,   800_000),
        "C": (400_000,   600_000),
        "D": (200_000,   400_000),
        "E":  (50_000,   200_000),
    }
    mn = rows.get(f"id_tier_{tier}_min", defaults[tier][0])
    mx = rows.get(f"id_tier_{tier}_max", defaults[tier][1])
    return mn, mx


def main():
    conn = sqlite3.connect(DB)
    conn.row_factory = sqlite3.Row
    c = conn.cursor()

    tiers = [("A", TIER_A), ("B", TIER_B), ("C", TIER_C), ("D", TIER_D), ("E", TIER_E)]
    added = {t: 0 for t, _ in tiers}
    skipped = {t: 0 for t, _ in tiers}
    total = 0

    for tier_name, ids in tiers:
        mn, mx = get_tier_range(c, tier_name)
        print(f"\n  Toifa {tier_name}: {len(ids)} ta ID, narx oralig'i {mn:,} — {mx:,} CODE")
        for cid in ids:
            total += 1
            # Foydalanuvchida band emasligini tekshir
            owned = c.execute("SELECT id FROM users WHERE custom_id=?", (cid,)).fetchone()
            if owned:
                skipped[tier_name] += 1
                continue
            existing = c.execute("SELECT id FROM premium_ids WHERE custom_id=?", (cid,)).fetchone()
            if existing:
                skipped[tier_name] += 1
                continue
            # Toifa oralig'idan random narx
            price = random.randint(mn, mx)
            c.execute(
                "INSERT INTO premium_ids (custom_id, id_type, base_price, status) VALUES (?,?,?,'available')",
                (cid, f"tier_{tier_name}", price)
            )
            added[tier_name] += 1

    conn.commit()
    conn.close()

    print("\n  ════════════════════════════════════")
    for t, _ in tiers:
        print(f"  Toifa {t}: +{added[t]} ta qo'shildi, {skipped[t]} ta o'tkazib yuborildi")
    print(f"  Jami: {sum(added.values())} ta yangi premium ID qo'shildi ({total} dan)")
    print("  ════════════════════════════════════")


if __name__ == "__main__":
    main()
